# Vamos recever dois dados diferentes do usuario. e concatenalas em uma String

# Solicitar o primeiro dado do usuário
dado1 = input("Digite o primeiro dado: ")

# Solicitar o segundo dado do usuário
dado2 = input("Digite o segundo dado: ")

# Concatenar os dois dados
resultado = dado1 + dado2

# Exibir o resultado
print("A concatenação dos dados é:", resultado)
